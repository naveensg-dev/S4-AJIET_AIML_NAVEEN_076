import java.util.Scanner;
class Monkeys{
    public static void main(String[] args) {
        int n = 20;
        int k = 2;
        int j = 3;
        int m = 12;
        int p = 12;
        int total;
        int d1 = m%k;
        int d2 = p%j;
       
        if (d1==0 && d2==0){
           int  a1 = m/k;
            int a2 = p/j;
            int q = a1+a2;
         total = n - q;
        }
        else {
            int a1 = m/k;
            int a2 = p/j;
            int q = a1+a2+d1+d2;
          total = n - q;
        }
       
        System.out.println(total);
       
    }
}
